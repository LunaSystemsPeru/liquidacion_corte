    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        cargarTabla();

        String fechafin = varios.fecha_myql(convertFecha());
        corte.setFecha(fechafin);
        corte.verCorteDia(modelo);

        //tbl_jornaleros.setModel(modelo);
        int contarfilas = tbl_jornaleros.getRowCount();
        jTextField1.setText(contarfilas + "");
        double sumapago = 0;
        double sumakilos = 0;
        for (int i = 0; i < contarfilas; i++) {
            sumapago += (Double.parseDouble(tbl_jornaleros.getValueAt(i, 4).toString()));
            sumakilos += (Double.parseDouble(tbl_jornaleros.getValueAt(i, 3).toString()));
        }
        jTextField2.setText(varios.formato_totales(sumapago));
        jTextField4.setText(varios.formato_totales(sumakilos));
        jTextField5.setText(varios.formato_totales(sumakilos / contarfilas));
    }//GEN-LAST:event_jButton6ActionPerformed

