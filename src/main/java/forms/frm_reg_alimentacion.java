    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //verificar si hay datos

        int errores = 0;
        if (jTextField6.getText().length() == 0) {
            Notification.show("ERROR", "No hay datos en el precio");
            errores++;
        }

        if (jTextField4.getText().length() == 0) {
            Notification.show("ERROR", "No hay datos en la cantidad");
            errores++;
        }

        if (jTextField5.getText().length() == 0) {
            Notification.show("ERROR", "No hay datos en la movilidad");
            errores++;
        }

        if (errores == 0) {
            o_combobox combo_alcliente = (o_combobox) cbx_tipo_alimentos.getSelectedItem();

            int totalitemstabla = jTable1.getRowCount();
            double preciounit = Double.parseDouble(jTextField6.getText());
            int cantidad = Integer.parseInt(jTextField4.getText());
            double subtotal = cantidad * preciounit;
            double movilidad = Double.parseDouble(jTextField5.getText());
            double total = movilidad + subtotal;
            
             String fechadb = c_varios.convertFecha(jDateChooser1);

            alimentacion.setCantporciones(cantidad);
            alimentacion.setFecha(fechadb);
            alimentacion.obtenerId();
            alimentacion.setIdplatillo(combo_alcliente.getId());
            alimentacion.setMovilidad(movilidad);
            alimentacion.setCostoplatillo(preciounit);
            alimentacion.insertar();


            Object filas[] = new Object[8];
            filas[0] = totalitemstabla + 1;
            filas[1] = combo_alcliente.getTexto();
            filas[2] = preciounit;
            filas[3] = cantidad;
            filas[4] = subtotal;
            filas[5] = movilidad;
            filas[6] = total;
            filas[7] = alimentacion.getId();
            modelo.addRow(filas);

            limpiar();
            sumartabla();
        }

    }//GEN-LAST:event_jButton2ActionPerformed


