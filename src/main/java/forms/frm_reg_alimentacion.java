    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        //leer todos los campos y llenar en tabla alimentacion cliente
        int nrofilas = jTable2.getRowCount();
        for (int i = 0; i < nrofilas; i++) {
            alcliente.setIditem(Integer.parseInt(jTable2.getValueAt(i, 3).toString()));
            if (alcliente.obtenerDatos()) {
                alcliente.setPrecio(Double.parseDouble(jTable2.getValueAt(i, 2).toString()));
                alcliente.modificar();
            } else {
                alcliente.setPrecio(Double.parseDouble(jTable2.getValueAt(i, 2).toString()));
                alcliente.insertar();
            }
        }
        jDialog1.dispose();
    }//GEN-LAST:event_jButton6ActionPerformed

