    private void setearFecha(String date) {
        try {
            java.util.Date date2;
            date2 = new SimpleDateFormat("yyyy-MM-dd").parse(date);
            dt_fecnacimiento.setDate(date2);
        } catch (java.text.ParseException ex) {
            System.out.println(ex.getLocalizedMessage());
        }
    }

    private boolean verificarDNI() {
        boolean continuar = true;
        String dni = txt_documento.getText().trim();
        obrero.setNrodocumento(dni);
        if (obrero.verificarDocumento()) {
            continuar = false;
            JOptionPane.showMessageDialog(null, "Este trabajador ya esta registrado.\nReiniciando Informacion");
            txt_documento.setText("");
            txt_documento.requestFocus();
        }
        return continuar;
    }

    private void llenar() {
        if (obrero.getIdobrero() == 0) {
            if (obrero.getNrocuenta().length() > 0) {
                obrero.setFecha_modificacion(varios.getFechaActual());
            } else {
                obrero.setFecha_modificacion("1000-01-01");
            }
        } else {
            if (!obrero.getNrocuenta().equals(txt_nrocuenta.getText().trim())) {
                obrero.setFecha_modificacion(varios.getFechaActual());
            }
        }

        obrero.setApepat(txt_apepat.getText().trim().toUpperCase());
        obrero.setApemat(txt_apemat.getText().trim().toUpperCase());
        obrero.setNombres(txt_nombres.getText().trim().toUpperCase());
        obrero.setNrodocumento(txt_documento.getText());
        obrero.setNrocuenta(txt_nrocuenta.getText().trim());
        String fechanacimiento = varios.fecha_myql(varios.convertFecha(dt_fecnacimiento));
        obrero.setFecha_nacimiento(fechanacimiento);

        if (jCheckBox1.isSelected()) {
            obrero.setNrodnititular(obrero.getNrodocumento());
        } else {
            obrero.setNrodnititular(txt_nrotitular.getText().trim());
        }
    }

    private void limpiar() {
        obrero.setIdobrero(0);
        txt_apepat.setText("");
        txt_apemat.setText("");
        txt_nombres.setText("");
        txt_documento.setText("");
        txt_nrocuenta.setText("");
        txt_nrotitular.setText("");
        jCheckBox1.setSelected(true);
        txt_nrotitular.setEnabled(false);
        txt_documento.requestFocus();
    }

