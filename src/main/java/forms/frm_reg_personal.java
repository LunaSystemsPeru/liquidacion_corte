    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (txt_documento.getText().length() == 8) {
            if (verificarDNI()) {
                try {
                    String json_datos = ApiPeruConsult.getJSONDNI(txt_documento.getText());
                    ArrayList datos = ApiPeruConsult.showJSONDNI(json_datos);
                    txt_nrotitular.setText(txt_documento.getText());
                    txt_apepat.setText(datos.get(0).toString());
                    txt_apemat.setText(datos.get(1).toString());
                    txt_nombres.setText(datos.get(2).toString());

                    txt_nrocuenta.requestFocus();

                } catch (ParseException ex) {
                    JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
                }
            }
        } else {
            Notification.show("Error Consulta", "NO HA INGRESADO INFORMACION");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

